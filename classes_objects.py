class Dog():
    """
    A class containing method bark
    """
    def bark(self):
        print(f'Dog is barking')


# An object of class Dog()
dog1 = Dog()

# Calling bark() method of dog1 object
dog1.bark()

# Creating another object of class Dog
dog2 = Dog()

# Calling bark() method of dog2 object
dog2.bark()
